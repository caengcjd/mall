package mystruts;

import java.util.HashMap;
import java.util.Map;

/**
 * 配置文件类
 * 
 * @author liuyong
 * 
 */
public class ActionMapping {
	private String name; // 数据bean的名字
	private String type; // 映射路径的类名
	private String path; // 映射路径
	private String input; // 入口的Jsp
	private Map<String, ActionForward> forwards = new HashMap<String, ActionForward>();// 跳转网页

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getInput() {
		return input;
	}

	public void setInput(String input) {
		this.input = input;
	}

	public Map<String, ActionForward> getForwards() {
		return forwards;
	}

	public void setForwards(Map<String, ActionForward> forwards) {
		this.forwards = forwards;
	}
}
